clear all;
load highway.mat;
p=[sqrs;sqjdcs;sqglmj];  %输入数据矩阵
t=[glkyl;glhyl];         %目标数据矩阵
%%
%利用函数premnmx对数据进行归一化
[pn,minp,maxp,tn,mint,maxt]=premnmx(p,t);%对输入矩阵p和输出矩阵t归一化处理
dx=[-1,1;-1 1;-1 1];                    %归一化处理后最小值为-1，最大值为1
%%
%利用处理好的数据，进行网络训练
%BP网络训练
%建立模型，并使用梯度下降法训练
net=newff(dx,[3,7,2],{'tansig','tansig','purelin'},'traingdx');

net.trainParam.show=10;              %1000轮回显示一次结果
net.trainParam.Lr=0.05;                %学习速率为0.05
net.trainParam.epochs=100;           %最大训练轮回为50000次
net.trainParam.goal=0.65*10^(-3);      %均方误差
net=train(net,pn,tn);                  %开始训练，pn为输入样本，tn输出样本
%%
%利用训练好的网络，数据仿真
an=sim(net,pn);                        %用训练好的模型进行仿真
a=postmnmx(an,mint,maxt);              %把仿真结果返归一化
%%
%用原始数据仿真结果与已知数据对比测试
%本例样本容量有限，所以只能用训练数据来测试
x=1990:2009;
newk=a(1,:);
newh=a(2,:);
figure(2);
subplot(2,1,1);plot(x,newk,'r-o',x,glkyl,'b--+');   %绘制公路客运量对比图
legend('网络输出客运量','实际客运量');
xlabel('年份');ylabel('客运量/万人');
title('运用工具箱 客运量学习和测试对比图');           
subplot(2,1,2);plot(x,newh,'r-o',x,glhyl,'b--+');   %绘制公路货运量对比图
legend('网络输出货运量','实际货运量');
xlabel('年份');ylabel('货运量/万吨');
title('运用工具箱 货运量学习和测试对比图');           %利用训练好的模型进行预测
%%
%利用训练好的BP，对新数据进行仿真

%利用训练好的网络进行预测
%当用训练好的网络对新的数据pnew进行预测时，也应做相应的处理
